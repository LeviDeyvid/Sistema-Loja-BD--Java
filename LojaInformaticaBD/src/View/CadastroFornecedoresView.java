    private void TxtPesquisaNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtPesquisaNomeKeyReleased
        // TODO add your handling code here:
       
        String nome = "%" +TxtPesquisaNome.getText() +"%";
        FornecedoresDao dao = new FornecedoresDao();
        List<FornecedoresBean> lista = dao.pesquisarNome(nome);
        DefaultTableModel dados =(DefaultTableModel)ListaDeFornecedoresjTable1.getModel();
        dados.setNumRows(0);
        
        for(FornecedoresBean c : lista){
            dados.addRow(new Object[]{
                c.getId(),
               c.getNome(),
               c.getCnpj(),
               c.getEmail(),
               c.getTelefone(),
               c.getCelular(),
              c.getCep(),
               c.getEndereco(),
               c.getNumero(),
              c.getComplemento(),
                c.getBairro(),
               c.getCidade(),
               c.getEstado()
            });
                  
        }
       
    }//GEN-LAST:event_TxtPesquisaNomeKeyReleased

