    public void ListarFornecedores(){
        FornecedoresDao dao = new FornecedoresDao();
        List<FornecedoresBean> lista = dao.ListarOsFornecedores();
        DefaultTableModel fornecedores = (DefaultTableModel) ListaDeFornecedoresjTable1.getModel();
        fornecedores.setNumRows(0);
        for(FornecedoresBean c:lista){
            fornecedores.addRow(new Object[]{
            
                c.getId(),
               c.getNome(),
               c.getCnpj(),
               c.getEmail(),
               c.getTelefone(),
               c.getCelular(),
              c.getCep(),
               c.getEndereco(),
               c.getNumero(),
              c.getComplemento(),
                c.getBairro(),
               c.getCidade(),
               c.getEstado()
        });
        }
    }
    
    
